package com.veysel;

public class GorevOperatorleri {

	public static void main(String[] args) {
		
		/*
		 * = -> atama işlemi yapar
		 * -=
		 * /=
		 * *=
		 * %=
		 */
		//ASSIGMENT OPERATORLERI
		int sayi=5;
		sayi=sayi+10;// eşitliğin sağındaki kısma ata diyoruz
		System.out.println("Sayi......"+sayi);
		
		sayi +=10;
		System.out.println("Sayi......"+sayi);
		
		sayi -=10;
		System.out.println("Sayi......"+sayi);
		
		sayi *=10;
		System.out.println("Sayi......"+sayi);
		
		sayi /=10;
		System.out.println("Sayi......"+sayi);
		
		sayi %=10;
		System.out.println("Sayi......"+sayi);
		
		
		
		
		
		
	

	}

}
