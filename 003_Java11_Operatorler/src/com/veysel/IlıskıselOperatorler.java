package com.veysel;

public class IlıskıselOperatorler {

	public static void main(String[] args) {
		
		/*
		 * İki değerin bir birine göre durumunu irdeler ve sonucu doğru/yanlış şeklinde bize iletir.
		 * 
		 * Burada yeni bir değişken karşımızı çıkıyor.
		 * 
		 * boolean değişkeni -> 1 bit lik değişkendir.Bir kutucuk 
		 * 
		 * int s1=25,s2=56;
		 * 2.sayı 1. sayıdan büyük müdür? evet/doğrudur -> true/false
		 * 
		 * 
		 * == -> İki bilginin bir birbirine eşit olup olmadığını irdeler ve sonuc döner
		 * 
		 * > -> büyüktür
		 * 
		 * < -> küçüktür
		 * 
		 * >= -> büyük eşittir.
		 * 
		 * <= -> küçük eşittir.
		 * 
		 * ! -> değil operatörü.
		 * 
		 *
		 * 
		 * 
		 * 
		 */
		
		
		int s1=5,s2=56;
		System.out.println("5 sayısı 56 sayısına eşit midir? "+(s1==s2));// hayır değildir.Sonuc false çıkar.
		
		System.out.println("5 sayısı 56 sayısına büyük müdür? "+(s1>s2));// hayır değildir.Sonuc false çıkar.
		
		System.out.println("5 sayısı 56 sayısına küçük müdür? "+(s1<s2));// evet değildir.Sonuc true çıkar.
		
		System.out.println("5 sayısı 56 sayısına küçük yada eşit midir? "+(s1<=s2));// evet değildir.Sonuc true çıkar.
		
		System.out.println("5 sayısı 56 sayısına büyük yada eşit midir? "+(s1>=s2));// hayır değildir.Sonuc false çıkar.
		
		System.out.println("5 sayısı 56 sayısına eşit değil midir? "+(s1!=s2));// evet değildir.Sonuc true çıkar.

		System.out.println("5 sayısı 56 sayısına eşit değil midir? "+(s1!=s2));// evet değildir.Sonuc true çıkar.

	
		
		
		
		
		
		
		
		
		// yazılımda her konu cok kolay ve mantıklıdır.Çok basit ve mantıklı düşün !!!!
		
		
		
		
		
		

	}

}
